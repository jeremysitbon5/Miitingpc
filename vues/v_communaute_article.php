            <p  style="margin-top: 5%;">Jupe bustier ou robe? Classique ou originale? Blanc ou ivoire ? Marque de créateur ou de fabricant ? Étole ou boléro ? La richesse des critères de choix de la robe de mariée vous complique parfois la tache. Pourtant, comment imaginer l'épouser dans une robe qui ne serait pas la robe de vos rêves? </p>
            <br><br>
            <p>Pour trouver la robe parfaite, suivez nos conseils. Pas de panique! Qu'on se le dise, choisir une robe de mariée ne s’apparente pas à un sport de combat ! <br>
            Les modèles ne s'envolent pas en une semaine. Ils vous attendent, exposés sur des cintres ou en photo dans des books. Vous les essayez dans des cabines vastes et éclairées. Les vendeuse sont là pour vous conseiller. Les retouches s'étalent sur plusieurs mois. Et le paiement de la robe peut se faire en plusieurs fois. Bref, rangez vos armes de "sérial acheteuse" et prenez le temps d'élaborer une stratégie avant de vous lancer à l'assaut des boutiques. L'achat d'une robe de mariée doit constituer l'une des plus grandes joies des préparatifs du mariage. A savourer lentement...</p>
            <p><strong>Le temps de lire et de flâner</strong></p>
            <p>bien calée dans un fauteuil, feuilletez les magazines, regardez les catalogues des marques. Visualiser la totalité des nouvelles collections. A partir de la, notez les marques correspondant à vos désirs. Tenez compte de votre morphologie. N'oubliez pas de lister les éléments que vous souhaiterez porter. Mettre vos idées par écrit vous aidera à expliquer  vos goûts le jour venu. Ensuite, c'est parti pour une première balade dans les magasins.</p>

            <p><strong>Juste pour voir </strong></p>
            <p>Bras dessus bras dessous avec votre maman, votre sœur ou votre meilleure amie, ouvrez grand les yeux, touchez tissus, consultez les books. Chez un créateur, renseignez-vous sur les possibilités de changement de couleur ou de coupe d'un modèle qui vous séduit. Soyez attentive aux conseils avisés des professionnels.
            Chez les fabricants, vous pourrez essayer certains robes sur le champ. Profitez -en pour tester les tenues les plus inattendues. C'est alors bien souvent la révélation dans le miroir. Néanmoins, laissez la nuit vous porter conseil et prévoyez quelque rendez vous pour tester le sur-mesure, histoire de pas avoir de regret. 
            Chez les créateurs, quinze jours à l'avance pour prendre rendez-vous. Dans les autres boutiques, tout dépend de la saison mais plus l'année scolaire avance, plus il est utile de réserver sa place. Retenez au maximum cinq lieux, à raison de cinq robes à enfiler pour chacun, sinon c'est la cassette assuré. Notez bien les références de robes que vous enfilez.</p>

            <p><strong>Robes de créateur ou de fabricant?</strong></p>
            <p>Chez un créateur, après une longue discussion avec le professionnel, tout est mis en oeuvre pour réaliser la robe idéale. Si  un modèle déjà existant vous séduit, il ne reste plus qu'à le personnaliser. A vous de choisir la forme du haut et du bas, le tissu, la couleur et d’accessoiriser votre robe. Dans le cas contraire, le créateur dessine avec vous un croquis de votre robe idéale afin de la concevoir du début à la fin. Ces experts s'adaptent à merveille à la demande de leurs clientes, mais c'est à vous de choisir celui qui correspond le mieux à votre style.
            </p>

            <p><strong>Attention au détails </strong></p>
            <p>
            Pour un mariage prévu en été, comptez au moins 6 mois entre le premier essayage et la finalisation de votre robe. En période plus creuse, trois mois peuvent suffire.  Chez un fabricant, les robes confectionnées sont vendu dans les enseignes à son nom ou par des revendeurs. Les retouches sont réalisées sur mesure, mais en général impossible de faire modifier complètement la coupe, la matière ou la couleur...  Vous obtenez votre robe en 48 heures si le modèle choisi est en rayon dans la bonne taille et qu'il nécessite presque pas d'ajustement. Mais, ce cas de figure étant rare, il faut en général se décider au moins trois mois à l'avance.
            </p>

            <p><strong>Réfléchissez bien avant de passer commande </strong></p>
            <p>Votre choix établi, livrez-vous sans remords à la prise des huit ou dix mesures indispensables à la confection. Comptez une heure par essayage. Le deuxième essayage permet d'ajuster la robe à votre silhouette. Un troisième, un quatrième et même un cinquième sont parfois nécessaires pour parfaire une création sophistiquée. Ne paniquez pas en voyant des bouts de tissu et des aiguilles un peu partout autour de vos membres !  A la dernière visite, vous découvrez réellement votre robe. Le moment de contrôler les détails : le choix des chaussures des bijoux... Vous ne repartez pas pour autant avec votre petite merveille !  Elle doit d'abord être nettoyée et repassée à la perfection. Patience...  
            </p>

